package org.acme.rest.json;

class Attributes {
    public String type;
    public String url;

    // Getter Methods 

    public String getType() {
        return type;
    }

    public String getUrl() {
        return url;
    }

    // Setter Methods 

    public void setType(String type) {
        this.type = type;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
